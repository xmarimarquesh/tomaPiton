'''O intuito do programa é ordenar uma lista, para isso
primeiro estamos declarando uma lista vazia, depois
pedimos ao usuário informar o tamanho da lista desejado.
Em seguida, criamos um laço de repetição para o usuário
preencher essa lista conforme o tamanho escolhido por ele.
Depois criamos dois novos laços de repetição aninhados 
para organizar essa lista de modo ordenado, fazendo a 
comparação entre o item atual e anterior da lista, e
trocando-os de lugar até comparar a lista inteira de modo
que fique ordenada.'''
